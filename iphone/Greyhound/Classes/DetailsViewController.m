-(void)setSchedule:(Schedule *)schedule {
	[_details removeAllObjects];
	ScheduleDetailsOperation *op = [[ScheduleDetailsOperation alloc] initWithSchedule:schedule delegate:self];
	[_opQueue addOperation:op];
	[[UIApplication sharedApplication] setNetworkActivityIndicatorVisible:YES];
}

-(void)detailsError:(NSError *)error {
}

-(void)detailsReady:(ScheduleDetails *)details {
	[_details addObject:details];
	[self.tableView reloadData];
}


#pragma mark -
#pragma mark Table view data source

- (NSInteger)numberOfSectionsInTableView:(UITableView *)tableView {
    // Return the number of sections.
    return 1;
}


- (NSInteger)tableView:(UITableView *)tableView numberOfRowsInSection:(NSInteger)section {
    // Return the number of rows in the section.
    return [_details count];
}


// Customize the appearance of table view cells.
- (UITableViewCell *)tableView:(UITableView *)tableView cellForRowAtIndexPath:(NSIndexPath *)indexPath {
    
    static NSString *CellIdentifier = @"Cell";
    
    UITableViewCell *cell = [tableView dequeueReusableCellWithIdentifier:CellIdentifier];
    if (cell == nil) {
        cell = [[[UITableViewCell alloc] initWithStyle:UITableViewCellStyleDefault reuseIdentifier:CellIdentifier] autorelease];
    }
	
	ScheduleDetails *details = [_details objectAtIndex:indexPath.row];
    [cell.textLabel setText:[NSString stringWithFormat:@"%@ %@ %@", details->cityName, details->arrives, details->departs]];
    
    return cell;
}


